// Returns holiday object {name, color} if date is a holiday for given nationalities
function getHolidayInfo(dateStr, nationalities = ['israel']) {
  let found = [];
  for (const nat of nationalities) {
    const hd = HOLIDAYS[nat];
    if (hd && hd.days.includes(dateStr)) {
      found.push({ name: hd.name, color: hd.color });
    }
  }
  return found.length > 0 ? found[0] : null;
}

function isShabbat(dateStr) {
  const d = new Date(dateStr + 'T12:00:00');
  return d.getDay() === 6;
}

function getNationalityHolidays(nationalityKey) {
  return HOLIDAYS[nationalityKey] || HOLIDAYS['india'];
}

